// ── Directions API fetch ─────────────────────────────────────────────────────
async function getDriveMinutes(origin, destination) {
  const url = new URL("https://maps.googleapis.com/maps/api/directions/json");
  url.searchParams.set("origin", `${origin.lat},${origin.lon}`);
  url.searchParams.set("destination", `${destination.lat},${destination.lon}`);
  url.searchParams.set("mode", "driving");
  url.searchParams.set("key", API_KEY);

  const res = await fetch(url.toString());
  const data = await res.json();

  if (data.status !== "OK") {
    throw new Error(`Directions API error: ${data.status} — ${data.error_message || ""}`);
  }

  const seconds = data.routes[0].legs[0].duration.value;
  return Math.round(seconds / 60);
}

// ── Build the matrix ─────────────────────────────────────────────────────────
async function buildMatrix() {
  const result = {};

  // Outbound: departure → arrival
  console.log("── Outbound (departure → arrival) ──────────────────────");
  for (const from of DEPARTURE_LOCATIONS) {
    result[from.id] = result[from.id] || {};
    for (const to of ARRIVAL_LOCATIONS) {
      try {
        const minutes = await getDriveMinutes(from, to);
        result[from.id][to.id] = minutes;
        console.log(`  ${from.id} → ${to.id}: ${minutes} min`);
      } catch (err) {
        console.error(`  ERROR ${from.id} → ${to.id}: ${err.message}`);
        result[from.id][to.id] = null;
      }
      // Stay well under the 10 QPS limit
      await new Promise(r => setTimeout(r, 120));
    }
  }

  // Return: arrival → departure
  console.log("\n── Return (arrival → departure) ────────────────────────");
  for (const from of ARRIVAL_LOCATIONS) {
    result[from.id] = result[from.id] || {};
    for (const to of DEPARTURE_LOCATIONS) {
      try {
        const minutes = await getDriveMinutes(from, to);
        result[from.id][to.id] = minutes;
        console.log(`  ${from.id} → ${to.id}: ${minutes} min`);
      } catch (err) {
        console.error(`  ERROR ${from.id} → ${to.id}: ${err.message}`);
        result[from.id][to.id] = null;
      }
      await new Promise(r => setTimeout(r, 120));
    }
  }

  return result;
}

// ── Write output ─────────────────────────────────────────────────────────────
const matrix = await buildMatrix();

const output = `/**
 * Drive time lookup table (minutes, no traffic, driving mode).
 * Generated by scripts/buildDriveTimes.mjs on ${new Date().toISOString().split("T")[0]}.
 * Do not edit manually — re-run the script to refresh.
 */
export const DRIVE_TIMES = ${JSON.stringify(matrix, null, 2)};

/**
 * Returns estimated drive time in minutes between two location IDs.
 * Returns null if the pair is not in the table.
 * @param {string} fromId
 * @param {string} toId
 * @returns {number|null}
 */
export function getDriveMinutes(fromId, toId) {
  return DRIVE_TIMES[fromId]?.[toId] ?? null;
}

/**
 * Given a departure time string ("HH:MM") and a from/to pair,
 * returns the estimated arrival time string ("HH:MM"), or null.
 * @param {string} departureTime  e.g. "06:30"
 * @param {string} fromId
 * @param {string} toId
 * @returns {string|null}
 */
export function estimateArrival(departureTime, fromId, toId) {
  const minutes = getDriveMinutes(fromId, toId);
  if (!minutes || !departureTime) return null;
  const [h, m] = departureTime.split(":").map(Number);
  const total = h * 60 + m + minutes;
  const ah = String(Math.floor(total / 60) % 24).padStart(2, "0");
  const am = String(total % 60).padStart(2, "0");
  return \`\${ah}:\${am}\`;
}
`;

const outPath = path.join(ROOT, "lib/drive-times.js");
fs.writeFileSync(outPath, output);
