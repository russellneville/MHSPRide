/**
 * buildDriveTimes.mjs
 *
 * Hits the Google Directions API for every departure→arrival pair and writes
 * the results to lib/drive-times.js as a static lookup table.
 *
 * Usage:
 *   node scripts/buildDriveTimes.mjs
 *
 * Requires GOOGLE_MAPS_API_KEY in environment or reads from
 * resources/google-maps-api-key.txt
 */

import fs from "fs";
